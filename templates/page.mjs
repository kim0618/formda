function wrapPage({ slug, title, bodyHTML, description }) {
  return `${head({
    title: `${title} | ${site.name}`,
    description: description,
    canonical: `/pages/${slug}.html`,
  })}
${header('')}
<main class="wrap">
  <div class="crumb"><a href="/">홈</a> › ${title}</div>
  <article class="legal-page">
    <h1>${title}</h1>
    ${bodyHTML}
  </article>
</main>
${footer()}
`;
}

